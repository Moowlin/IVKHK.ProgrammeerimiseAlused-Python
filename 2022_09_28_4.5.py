'''
Пользователь вводит три числа – длины сторон треугольника.
Программа должна сообщить пользователю:
a. существует ли вообще такой треугольник (такого треугольника
не может быть, если сумма любых двух сторон окажется меньше
третьей стороны);
b. является ли треугольник прямоугольным;
c. является ли треугольник равносторонним;
d. является ли треугольник равнобедренным;
e. является ли треугольник разносторонним
'''
print('введите значения сторон треугольника')
a, b, c = int(input()), int(input()), int(input())

if (a + b < c) or (a + c < b) or (b + c < a):
    print('такого треугольника не существует')
elif a == b == c :
    print("это равносторонний треугольник")
elif (a == b != c) or (a != b == c) or (a == c != b):
    print('это равнобедренный треугольник')
elif (a**2 + b**2 == c**2) or (b**2 + c**2 == a**2) or (a**2 + c**2 == b**2):
    print('это прямоугольный треугольник')
elif a != b != c:
    print('это разносторонний треугольник')




'''
Напишите программу, которая в зависимости от введенного возраста
добавляет слова «год», «года» или «лет». Например, при вводе
возраста 1, программа сообщает «1 год», при числе 2 – «2 года», при
числе 125 – «125 лет»
'''

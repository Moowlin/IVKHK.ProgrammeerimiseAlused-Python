'''
Имеется девятиэтажный дом, на каждую лестничную площадку
выходит 4 квартиры. Создайте программу, которая по номеру
квартиры определяет номер подъезда и этаж.
'''
kv = int(input('Введите номер квартиры '))
p = kv // 36
floor = kv // 4
print('Подъезд №', p+1)
print('Этаж №', floor+1)




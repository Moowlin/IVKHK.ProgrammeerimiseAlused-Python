# Даны четыре действительных числа: x1, y1, x2, y2. Напишите функцию distance (x1, y1, x2, y2),
# вычисляющая расстояние между точкой (x1,y1) и (x2,y2). Считайте четыре действительных числа и
# выведите результат работы этой функции. Если вы не знаете, как решить эту задачу, то вы, возможно,
# не изучали в школе теорему Пифагора. Попробуйте прочитать о ней на Википедии.
# -------------------------------------------------------------------------------------------------

def distance(x1, y1, x2, y2):
    import math
    rez = math.sqrt(pow((x1-x2), 2) + pow((y1-y2), 2))
    return rez

x1, y1, x2, y2 = int(input()), int(input()), int(input()), int(input())
print(distance(x1, y1, x2, y2))

# -----------------------------= ЗАДАЧА «ОТРИЦАТЕЛЬНАЯ СТЕПЕНЬ» =----------------------------------
# Дано действительное положительное число a и целоe число n. Вычислите an.
# Решение оформите в виде функции power(a, n). Стандартной функцией возведения в степень пользоваться нельзя.
# -------------------------------------------------------------------------------------------------

def power(a, n):
    rez = 1
    for i in range(n):
        rez *= a
    return rez

a = int(input('введите число а '))
n = int(input('введите число n '))
print(power(a, n))

